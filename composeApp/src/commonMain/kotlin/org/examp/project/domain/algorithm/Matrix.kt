/**
 * 螺旋矩阵
 */
fun spiralOrder(matrix: Array<IntArray>): IntArray {
    if (matrix.isEmpty()) return intArrayOf()
    val m = matrix.size
    val n = matrix[0].size
    val result = IntArray(m * n)
    var index = 0
    var left = 0
    var right = n - 1
    var top = 0
    var bottom = m - 1

    while (left <= right && top <= bottom) {
        for (i in left..right) {
            result[index++] = matrix[top][i]
        }
        top++
        for (i in top..bottom) {
            result[index++] = matrix[i][right]
        }
        right--
        if (top > bottom || left > right) {
            break
        }
        for (i in right downTo left) {
            result[index++] = matrix[bottom][i]
        }
        bottom--
        for (i in bottom downTo top) {
            result[index++] = matrix[i][left]
        }
        left++
    }
    return result
}

/**
 * 基础类问题（必问，考察解法理解）
 * 问：本题的时间和空间复杂度分别是多少？为什么？
 * 答：时间复杂度 O (mn)（m 是行数，n 是列数），因为需要遍历矩阵中所有 mn 个元素，每个元素仅访问一次；空间复杂度 O (1)（除结果集），因为仅使用了 4 个边界变量和循环变量，无额外空间开销。
 * 问：为什么要在从右到左遍历前加边界判断？不加会有什么问题？
 * 答：为了避免单行 / 单列矩阵的重复遍历。比如单行矩阵[[1,2,3]]，完成 “左到右” 遍历后top++，此时top > bottom，如果继续执行从右到左和从下到上遍历，会重复访问元素 2，导致结果错误。
 * 问：如果去掉所有边界收缩的代码，会出现什么问题？
 * 答：会进入死循环，且不断重复添加矩阵的边界元素（如一直遍历第一行和最后一列），最终导致结果集无限增大并抛出异常。
 * 变形拓展类问题（高频，考察举一反三能力，客户端面试重点）
 * 问：如果要求顺时针生成螺旋矩阵（LeetCode 59，螺旋矩阵 II），该怎么修改代码？
 * 答：核心思路还是边界收缩法，区别在于：
 * 先创建一个 n×n 的空矩阵；
 * 定义一个当前要填充的数字（从 1 到 n²）；
 * 按相同的遍历顺序，将数字填充到矩阵对应位置，而非添加到结果集，其余边界收缩逻辑完全一致。
 * （可快速口述核心代码，无需完整书写，面试官主要看思路迁移能力）
 * 问：如果要求逆时针螺旋遍历矩阵，该如何调整遍历顺序？
 * 答：只需将四轮遍历的顺序调整为：从上到下→从右到左→从下到上→从左到右，边界收缩的时机和逻辑保持不变，核心是调整遍历方向的优先级。
 * 问：如果矩阵是三维的，螺旋遍历的思路是否适用？
 * 答：核心思路（边界收缩 + 分层遍历）依然适用，三维矩阵可按 “层” 遍历，每层按二维螺旋的方式处理，遍历完一层后收缩三维边界（如深度边界），但实际开发中极少用到，客户端面试仅考察思路。
 * 优化与思考类问题（考察思维深度）
 * 问：除了边界收缩法，还有其他解法吗？各有什么优缺点？
 * 答：有两种常见解法，但都不如边界收缩法优：
 * 方法 1：标记法（开辟一个和原矩阵相同大小的布尔矩阵，标记元素是否被访问），遍历到每个元素时判断下一个方向是否越界 / 已访问，若则转向；缺点：额外占用 O (mn) 空间，空间复杂度高，客户端开发不推荐。
 * 方法 2：递归法（将螺旋遍历拆解为 “遍历边界 + 递归处理内部子矩阵”），缺点：对于大矩阵会出现栈溢出，且递归的空间复杂度为 O (max (m,n))（递归深度），不如迭代法稳定。
 * 综上，边界收缩法是时间和空间最优的解法，也是工业界最常用的方法。
 * 问：如果矩阵的行和列非常大（如 10^4 × 10^4），你的解法会有问题吗？
 * 答：不会有问题。因为解法是原地遍历，空间复杂度为 O (1)，不会因矩阵过大导致内存溢出；时间复杂度是最优的 O (mn)，遍历效率达到理论上限，适合大数据量场景。
 * 工程与迁移类问题（贴合客户端开发，高频）
 * 问：在客户端开发中，这种 “边界收缩 + 多方向遍历” 的思路能用到哪些场景？
 * 答：客户端开发中很多UI 绘制、数据遍历场景会用到：
 * 自定义 View 时的螺旋绘制（如螺旋进度条、螺旋文字布局）；
 * 网格列表（如 RecyclerView 网格布局）的特殊遍历加载（如从中心向外螺旋加载数据）；
 * 图片处理中的像素矩阵遍历（如螺旋式模糊、滤镜处理）。
 * 问：如果让你封装一个通用的螺旋遍历工具方法，你会考虑哪些设计点？
 * 答：会按客户端开发的可复用性要求设计：
 * 增加遍历方向参数（顺时针 / 逆时针），支持多方向；
 * 增加非空校验和异常抛出（如空矩阵抛出 IllegalArgumentException，而非返回空列表）；
 * 泛型支持：不仅支持 Int 矩阵，还支持 String、Double 等类型的二维数组；
 * 返回值可选：支持返回 List 或数组（满足不同业务场景）。
